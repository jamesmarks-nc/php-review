<?php

// Arithmetic operators
echo "Ex 1. " . (1 + 1) . "<br>\n";    // 2
echo "Ex 2. " . (10 - 5) . "<br>\n";   // 5
echo "Ex 3. " . (10 * 10) . "<br>\n";  // 100
echo "Ex 4. " . (100 / 25) . "<br>\n"; // 4
// %: modulo operator - gets remainder after division
echo "Ex 5. " . (100 % 21) . "<br>\n"; // 16 (remainder after dividing 100 by 21)
// **: exponentiation operator - ensure two asterisks, no spaces
echo "Ex 6. " . (10 ** 2) . "<br>\n"; // 100 (10 to the power of 2)